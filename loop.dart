void main() {
  // For loop
  print('Using for loop:');
  for (int i = 1; i <= 5; i++) {
    print('Number: $i');
  }

  // While loop
  print('\nUsing while loop:');
  int j = 1;
  while (j <= 5) {
    print('Number: $j');
    j++;
  }

  // Do-while loop
  print('\nUsing do-while loop:');
  int k = 1;
  do {
    print('Number: $k');
    k++;
  } while (k <= 5);
}
